package main

import "fmt"

func main() {

	fmt.Println('♥')         // 9829
	fmt.Printf("%v\n", '♥')  // 9829
	fmt.Printf("%#v\n", '♥') // 9829
	fmt.Printf("%q\n", '♥')  // '♥'
	fmt.Printf("%U\n", '♥')  // '♥'
	fmt.Printf("%x\n", '♥')  // '♥'
	fmt.Printf("%d\n", '♥')  // '♥'
	fmt.Printf("%b\n", '♥')  // '♥'
	fmt.Printf("%o\n", '♥')  // '♥'
	fmt.Printf("%x\n", '♥')  // '♥'
	fmt.Printf("%s\n", "♥")  // '♥'

}
